/**
 * Centralized Axios instance — every service imports this instead of raw axios.
 * Base URL points at the Express backend mounted at /api.
 */
import axios from 'axios';

const api = axios.create({
  baseURL: 'http://localhost:5000/api',
  headers: {
    'Content-Type': 'application/json',
  },
});

// Attach JWT from localStorage on every request (used by protected endpoints later)
api.interceptors.request.use((config) => {
  const token = localStorage.getItem('token');
  if (token) {
    config.headers.Authorization = `Bearer ${token}`;
  }
  return config;
});

export default api;
